  if (["메오","돔페리뇽","오베르"].includes(crafter))               return e["TG_" + "TOKEN_MEO"];
  if (crafter === "포므리")                                          return e["TG_" + "TOKEN_POMMERY"];
  if (crafter === "뒤작")                                            return e["TG_" + "TOKEN_DUIJAC"];
  if (crafter === "디켐")                                            return e["TG_" + "TOKEN_DIKEM"];
  if (crafter === "크뤼그")                                          return e["TG_" + "TOKEN_KRUG"];
